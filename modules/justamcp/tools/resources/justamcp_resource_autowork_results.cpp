#ifdef TOOLS_ENABLED

#include "justamcp_resource_autowork_results.h"
#include "core/io/file_access.h"
#include "core/os/os.h"

void JustAMCPResourceAutoworkResults::_bind_methods() {}

JustAMCPResourceAutoworkResults::JustAMCPResourceAutoworkResults() {}
JustAMCPResourceAutoworkResults::~JustAMCPResourceAutoworkResults() {}

String JustAMCPResourceAutoworkResults::get_uri() const {
	return "autowork://latest_results";
}

String JustAMCPResourceAutoworkResults::get_name() const {
	return "Autowork Latest Results";
}

bool JustAMCPResourceAutoworkResults::is_template() const {
	return false;
}

Dictionary JustAMCPResourceAutoworkResults::get_schema() const {
	Dictionary result;
	result["uri"] = get_uri();
	result["name"] = get_name();
	result["description"] = "Streams the latest JUnit/JSON test results generated by the Autowork Godot testing framework directly into memory.";
	result["mimeType"] = "application/json";
	return result;
}

Dictionary JustAMCPResourceAutoworkResults::read_resource(const String &p_uri) {
	Dictionary result;

	if (p_uri != get_uri()) {
		result["ok"] = false;
		result["error_code"] = -32602;
		result["error"] = "Invalid URI handled by AutoworkResults Resource.";
		return result;
	}

	String content = "No recent test logs have been found for the Autowork directory in this project context.";
	// Check user space or res space for autowork output logs natively.
	String p_json = "user://autowork_results.json";
	String p_xml = "user://autowork_results.xml";

	if (FileAccess::exists(p_json)) {
		Ref<FileAccess> f = FileAccess::open(p_json, FileAccess::READ);
		if (f.is_valid()) {
			content = f->get_as_utf8_string();
		}
	} else if (FileAccess::exists(p_xml)) {
		Ref<FileAccess> f = FileAccess::open(p_xml, FileAccess::READ);
		if (f.is_valid()) {
			content = f->get_as_utf8_string();
		}
	}

	Array contents;
	Dictionary item;
	item["uri"] = p_uri;
	item["mimeType"] = "text/plain";
	item["text"] = content;
	contents.push_back(item);

	result["contents"] = contents;
	result["ok"] = true;
	return result;
}

#endif // TOOLS_ENABLED
